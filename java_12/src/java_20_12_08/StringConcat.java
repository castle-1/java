package java_20_12_08;

public class StringConcat {

	public static void main(String[] args) {
		
		String st1 = "Coffee";
		String st2 = "Bread";
		
		String st3 = st1.concat(st2); //문자열을 옆으로 연결
		System.out.println(st3);
		
		String st4 = "Fresh".concat(st3);
		System.out.println(st4);

	}

}
