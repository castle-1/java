package java_20_11_26;

public class Ex09 {

	public static void main(String[] args) {
		
		int num = 5;
		
		System.out.print((num++) + " ");//하나의 실행단위가 끝나게 되면 증가
		System.out.print((num++) + " ");
		System.out.print(num + "\n");
		
		System.out.print((num--) + " ");
		System.out.print((num--) + " ");
		System.out.print(num + "\n");

	}

}
