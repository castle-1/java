package java_20_11_26;

public class Ex03 {

	public static void main(String[] args) {
		
		int num1 = 7;
		int num2 = 3;
		
		System.out.println("num1 + num2 = " + (num1+num2));
		System.out.println("num1 - num2 = " + (num1-num2));
		System.out.println("num1 * num2 = " + (num1*num2));
		System.out.println("num1 / num2 = " + (num1/num2)); //몫이 나오는 이유는 int로 형변환
		System.out.println("num1 % num2 = " + (num1%num2));

	}

}
